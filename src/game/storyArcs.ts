export const YEAR_THEMES: Record<string, MarketTheme[]> = {
  "2023": THEMES_2023,
  "2024": THEMES_2024,
  "2025": THEMES_2025,
  "demo": THEMES_DEMO,
};

export function getTheme(year: string, monthIndex: number): MarketTheme {
  const themes = YEAR_THEMES[year] || THEMES_2024;
  return themes[monthIndex % themes.length];
}

